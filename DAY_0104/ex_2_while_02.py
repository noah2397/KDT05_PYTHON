#---------------------------------------
# 반복문 - 2 while 반복문
# - 반복의 횟수가 정해진 경우 사용 가능함
#---------------------------------------
# [요청] 사용자가 알고 싶어하는 단을 입력 받아서 해당 단의 구구단을 출력하기
#       단, while 반복문 사용사기
# [예시] 알고 싶은 단 입력 :
#       -- 3단 --
#       3 * 1 = 3
#       3 * 2 = 6
#       3 * 3 = 9
#       3 * 4 = 12
#       3 * 5 = 15
#       3 * 6 = 18
#       3 * 7 = 21
#       3 * 8 = 24
#       3 * 9 = 27
#--------------------------------------
'''
dan, i=int(input()),0

while i<10 :
    print(f"{dan} * {i} = {dan*i:2}") if i else print(f"  {dan:-^7}  ")
    i+=1
'''

#--------------------------------------
#       -- 2단 --
#       2 * 1 = 2
#       2 * 2 = 4
#       2 * 3 = 6
#           :
#       -- 3단 --
#       3 * 1 = 3
#       3 * 2 = 6
#       3 * 3 = 9
#           :
#       -- 4단 --
#       4 * 1 = 4
#       4 * 2 = 8
#       4 * 3 = 12
#           :
#           :
#       -- 9단 --
#       9 * 1 = 9
#       9 * 2 = 18
#       9 * 3 = 27
#           :
#--------------------------------------

i,j,dan=2,0,'단'
while i<10 :
    while j<10 :
        # --2단-- <-얘를 출력할 때, --2 는 오른쪽 정렬로, 단--은 왼쪽 정렬로 표시한다
        print(f"{i} * {j} = {i*j:2}") if j else print(f"  {i:->3}{dan:-<3}  ")
        j+=1
    i,j=i+1,0